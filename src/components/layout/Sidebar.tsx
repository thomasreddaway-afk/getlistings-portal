export function Sidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const { user, signOut } = useAuth();
  const [isMobileMenuOpen, setIsMobileMenuOpen] = useState(false);

  const NavLink = ({ item }: { item: NavItem }) => {
    const isActive = pathname === item.href || 
      (item.href !== '/' && pathname.startsWith(item.href));

    return (
      <Link
        href={item.href}
        className={cn(
          'w-full flex items-center space-x-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors relative',
          isActive 
            ? 'bg-primary/10 text-primary' 
            : 'text-gray-700 hover:bg-gray-100'
        )}
        onClick={() => setIsMobileMenuOpen(false)}
      >
        {item.icon}
        <span>{item.name}</span>
        {item.badge && (
          <span className={cn(
            'absolute right-3 text-white text-[10px] px-1.5 py-0.5 rounded font-bold uppercase tracking-wide',
            item.badgeColor || 'bg-primary'
          )}>
            {item.badge}
          </span>
        )}
      </Link>
    );
  };

  const NavSection = ({ title, items }: { title: string; items: NavItem[] }) => (
    <div className="pt-4">
      <p className="px-3 text-xs font-semibold text-gray-400 uppercase tracking-wider mb-2">{title}</p>
      {items.map((item) => (
        <NavLink key={item.href} item={item} />
      ))}
    </div>
  );

  const sidebarContent = (
    <>
      {/* Logo */}
      <div className="px-4 pt-[22px] pb-5 border-b border-gray-200">
        <div className="flex items-center space-x-3">
          <div className="w-10 h-10 bg-primary rounded-lg flex items-center justify-center">
            <svg className="w-6 h-6 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M3 12l2-2m0 0l7-7 7 7M5 10v10a1 1 0 001 1h3m10-11l2 2m-2-2v10a1 1 0 01-1 1h-3m-6 0a1 1 0 001-1v-4a1 1 0 011-1h2a1 1 0 011 1v4a1 1 0 001 1m-6 0h6" />
            </svg>
          </div>
          <div>
            <h1 className="font-bold text-gray-900">Get Listings</h1>
            <p className="text-sm text-gray-500">Desktop Portal</p>
          </div>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4 space-y-1 overflow-y-auto">
        {/* Dashboard */}
        <Link
          href="/"
          className={cn(
            'w-full flex items-center space-x-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors',
            pathname === '/' 
              ? 'bg-primary/10 text-primary' 
              : 'text-gray-700 hover:bg-gray-100'
          )}
          onClick={() => setIsMobileMenuOpen(false)}
        >
          <DashboardIcon />
          <span>Dashboard</span>
        </Link>

        <NavSection title="Lead Generation" items={leadGenItems} />
        <NavSection title="Marketing" items={marketingItems} />
        <NavSection title="Insights" items={insightsItems} />
        <NavSection title="Growth" items={growthItems} />
        <NavSection title="Other" items={otherItems} />
      </nav>

      {/* User */}
      <div 
        className="p-4 border-t border-gray-200 cursor-pointer hover:bg-gray-50"
        onClick={() => router.push('/settings')}
      >
        <div className="flex items-center space-x-3">
          <div className="w-9 h-9 bg-primary rounded-full flex items-center justify-center text-white font-medium">
            {user?.first_name?.[0]}{user?.last_name?.[0] || ''}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-gray-900 truncate">
              {user?.first_name} {user?.last_name}
            </p>
            <p className="text-xs text-gray-500 truncate">
              {user?.role === 'admin' ? 'Administrator' : 
               user?.role === 'staff' ? 'Staff' : 'Agent'}
            </p>
          </div>
          <button
            onClick={(e) => {
              e.stopPropagation();
              signOut();
            }}
            className="p-2 text-gray-400 hover:text-gray-600 hover:bg-gray-100 rounded-lg"
            title="Sign out"
          >
            <LogOutIcon />
          </button>
        </div>
      </div>
    </>
  );

  return (
    <>
      {/* Mobile menu button */}
      <button
        className="lg:hidden fixed top-4 left-4 z-50 p-2 bg-white rounded-lg shadow-md"
        onClick={() => setIsMobileMenuOpen(!isMobileMenuOpen)}
      >
        {isMobileMenuOpen ? <CloseIcon /> : <MenuIcon />}
      </button>

      {/* Mobile sidebar overlay */}
      {isMobileMenuOpen && (
        <div 
          className="lg:hidden fixed inset-0 bg-black/20 z-40"
          onClick={() => setIsMobileMenuOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={cn(
          'fixed lg:static inset-y-0 left-0 z-40 w-72 bg-white border-r border-gray-200 flex flex-col transition-transform duration-200',
          isMobileMenuOpen ? 'translate-x-0' : '-translate-x-full lg:translate-x-0'
        )}
      >
        {sidebarContent}
      </aside>
    </>
  );
}
